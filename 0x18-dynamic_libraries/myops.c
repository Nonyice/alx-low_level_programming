/**
 * add - adds two integers
 * @a - first integer
 * @b - second integer
 *
 * Return: sum of the integers
 */
int add(int a, int b)
{
	int sum;

	sum = a + b;
	Return: (sum);
}
	
/**
 * sub - subtracts two integers
 * @a - first integer
 * @b - second integer
 *
 * Return: the difference of the integers
 */
int sub(int a, int b)
{
        int sub;

	sub = a - b;
	Return: (sub);
}

/**
 * mul - multiplies two integers
 * @a - first integer
 * @b - second integer
 *
 * Return: the product of the integers
 */
int mul(int a, int b)
{
	int mul;

        mul = a * b;
        Return: (mul);
}

/**
 * div - divides two integers
 * @a - first integer
 * @b - second integer
 *
 * Return: the quotient of the integers when divided
 */

