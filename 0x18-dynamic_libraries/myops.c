/**
 * mod - finds the remainder when two integers are divided
 * @a - first integer
 * @b - second integer
 *
 * Return: remainder of the integers division
 */

int mod(int a, int b)
{
	int mod;
	
	mod = a % b;
        Return: (mod);
}
